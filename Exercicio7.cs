using System;

class Exercicio6
{

    static void Main (string[] args) { 

     for(int numero = 1; numero <= 10; numero++){
        Console.WriteLine($"Tabuada do {numero}");

     for(int i = 1; i <= 10; i++)  
        Console.WriteLine($"{numero} x {i} = {numero * i}");
    }  

    }  
}


